// WAJP to print all the numbers from 10 to 1.
public class Q2 {

    public static void printNumber(){

        int i = 10;

        while(i>=1){
            System.out.println("The number Is :"+i);
            i--;
        }
    }

    public static void main(String[] args) {
        printNumber();
    }

    
}
